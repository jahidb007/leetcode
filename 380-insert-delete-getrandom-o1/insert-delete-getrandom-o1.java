class RandomizedSet {
    Set<Integer> set;
    public RandomizedSet() {
        set = new HashSet<>();
    }
    
    public boolean insert(int val) {
        return set.add(val);
    }
    
    public boolean remove(int val) {
        return set.remove(val);
    }
    
    public int getRandom() {
        int idx = (int) (Math.random() * set.size());
        
        Object[] objects = set.toArray();
        return (int) objects[idx];
    }
}

